<?php }



// Create 1 Custom Post type for a Demo, called HTML5-Blank
function create_post_type_html5()
{
    register_taxonomy_for_object_type('category', 'html5-blank'); // Register Taxonomies for Category
    register_taxonomy_for_object_type('post_tag', 'html5-blank');
    register_post_type('html5-blank', // Register Custom Post Type
        array(
        'labels' => array(
            'name' => __('HTML5 Blank Custom Post', 'evos'), // Rename these to suit
            'singular_name' => __('HTML5 Blank Custom Post', 'evos'),
            'add_new' => __('Add New', 'evos'),
            'add_new_item' => __('Add New HTML5 Blank Custom Post', 'evos'),
            'edit' => __('Edit', 'evos'),
            'edit_item' => __('Edit HTML5 Blank Custom Post', 'evos'),
            'new_item' => __('New HTML5 Blank Custom Post', 'evos'),
            'view' => __('View HTML5 Blank Custom Post', 'evos'),
            'view_item' => __('View HTML5 Blank Custom Post', 'evos'),
            'search_items' => __('Search HTML5 Blank Custom Post', 'evos'),
            'not_found' => __('No HTML5 Blank Custom Posts found', 'evos'),
            'not_found_in_trash' => __('No HTML5 Blank Custom Posts found in Trash', 'evos')
        ),
        'public' => true,
        'hierarchical' => true, // Allows your posts to behave like Hierarchy Pages
        'has_archive' => true,
        'supports' => array(
            'title',
            'editor',
            'excerpt',
            'thumbnail'
        ), // Go to Dashboard Custom HTML5 Blank post for supports
        'can_export' => true, // Allows export in Tools > Export
        'taxonomies' => array(
            'post_tag',
            'category'
        ) // Add Category and Post Tags support
    ));
}

